<h1>PING d.o.o. Sarajevo</h1>
<p> 
</br>
Ping doo je preduzeće za računarski i informatički inžinjering, osnovano 19.02.1997. od strane grupe stručnjaka iz oblasti računarstva i 
informatike. Osnivači su se sakupili oko ideje da stvore efikasno preduzeće, sposobno da pruži usluge u gotovo svakoj oblasti računarskog i 
informatičkog inžinjeringa, na osnovu komplementarnih znanja i iskustava, sa posebnim naglaskom na kvalitet u svakom koraku koji se poduzima. 
Primarno tržišno opredjeljenje PING d.o.o. su veliki i složeni privredni ili vladini sistemi (banke, trgovinska preduzeća, proizvodna preduzeća, 
ministarstva, vladine i druge agencije, univerzitetske organizacije i slično).</p>
<p>
Partnerski ugovori sa svjetski poznatim firmama iz IT oblasti koje PING d.o.o. stalno održava, su potpuno u suglasnosti sa osnovnom orjentacijom 
na kvalitet – PING d.o.o. želi pružiti rješenja koja će u potpunosti zadovoljiti očekivanja korisnika. Standardni izbor platformi je:
<ul>
<li>Oracle softver (sistem za upravljanje bazama podataka i razvojni alati)</li>
<li>HP računari i aktivna mrežna oprema</li>
<li>CISCO aktivna mrežna oprema</li>
<li>Alcatel-Nexans pasivna mrežna oprema</li>
<li>Microsoft softver (operativni sistemi, razvojni alati i aplikacije)</li>
<li>ActivCard i GFI zaštita od neovlaštenog pristupa.</li>
</ul>
</p>
<p>
</br>
<b>Ideja</b><br>
PING se vodi poslovnom idejom "Sveobuhvatna Usluga i Cjelovita Rješenja", jer posjeduje jedinstveno iskustvo i nivo stručnosti u oblastima:
<ul>
<li>analize, projektovanja, razvoja i implementacije proizvoljno složenih informacionih sistema i primjena baza podataka</li>
<li>konfiguracije, instaliranja i održavanja računarskih sistema, operativnih sistema i distribuiranih sistema za upravljanje bazama podataka</li>
<li>projektovanja, konfiguracije, instaliranja i održavanja računarskih mreža – lokalnih (LAN) i globalnih (WAN) mrežnih sistema.</li>
</ul>
</p>
<p>
</br>
<b>Strategija</b><br>
Strateški gledano PING d.o.o. usmjerava svoje djelovanje u nekoliko pravaca:
<ul>
<li>Projektovanje i realizacija distribuiranih sistema za upravljanje bazama podataka (Oracle)</li>
<li>Projektovanje i realizacija lokalnih i globalnih mrežnih računarskih sistema (Cisco, 3com, Nexans-Alcatel)</li>
<li>Projektovanje i realizacija uvezivanja lokalnih i globalnih sistema u/na Internet i Internet (Cisco i Microsoft)</li>
<li>Konsalting, projektovanje, implementacija i fino podešavanje sistemskog softvera (Microsoft)</li>
<li>Distribucija i prodaja proizvoda firmi (HP Compaq, Cisco, Microsoft, Oracle, Activcard i GFI)</li>
<li>Projektovanje i realizacija sistema za autentifikaciju i sigurnost (Cisco, ActivCard)</li>
<li>Pružanje podrške i servisa za sve proizvode iz PING d.o.o. proizvodnog programa</li>
<li>Projektovanje, inženjering i konsalting usluge u oblasti informacionih tehnologija</li>
<li>Obuka korisnika</li>
</ul>
</p>
